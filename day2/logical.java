public class logical {
    public static void main(String[] args){
        // and operator &&

        System.out.println((5>4)&&(5>1));// true
        System.out.println((5>4)&&(5>10));// false
/// OR operations
        System.out.println((5>4)|| (5>1));// true
        System.out.println((5>14)|| (5>10));// false
    }
}
