public class Switch {
    public static void main(String[] args){
        int num = 12;

        switch (num) {
            case 1:
            System.out.println("case 1 execute");
            break;
            case 2:
            System.out.println("case 2 exe");
            break;
            default:
            System.out.println("there is nothing inside the switch case");
        }

    }
}
